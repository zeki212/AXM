      <p>第十七届（2020）中国慈善榜发布</p>
      <p>2020年6月16日</p>
      <p>&ensp;&ensp;6月16日，第十七届（2020）中国慈善榜正式发布。为做好新冠肺炎疫情防控工作，本次发布采用了线上直播的形式。</p>
　　  <p>&ensp;&ensp;本届中国慈善榜上榜慈善家118位（对），合计捐赠54.5036亿元，共有十位慈善家荣获个人奖项；上榜慈善企业605家，合计捐赠124.0590亿元。同时揭晓的还有中国慈善明星榜单，记录了100位明星的慈善事迹。《2019中国慈善捐赠发展蓝皮书》同期发布，对2019年度我国慈善事业的发展进行了全景式的描述。此外，为了记录2020年中国人民同舟共济、守望相助、同心战疫的历程，活动还发布了抗击新冠肺炎疫情捐赠榜单。</p>
　　<p>&ensp;&ensp;在榜单发布的基础上，主办方对年度慈善家、慈善企业、基金会、慈善项目、慈善明星、公益研究报告、慈善榜样等进行了表彰。</p>
　<p>&ensp;&ensp;全国人大社会建设委员会副主任委员、民政部原副部长、中华慈善总会会长宫蒲光，中国侨联顾问、中国华侨公益基金会理事长乔卫，中国社会工作联合会副会长兼秘书长、《公益时报》社社长刘京，中国社会工作联合会副会长何建民等见证了榜单发布。</p>
　　<p>&ensp;&ensp;为了旗帜鲜明地扬善，此次活动在通过公益时报主页面进行直播的同时，通过新华网、央视网等18个平台对外直播。</p>
　　<p>&ensp;&ensp;<b>以捐赠守望相助</b></p>
　　<p>&ensp;&ensp;中国慈善榜由《公益时报》社自2004年开始每年编制发布。榜单以寻找榜样的力量、弘扬现代公益精神为宗旨，以年度实际捐赠100万元以上的企业或个人为数据采集样本，被誉为中国财富人士的爱心清单。</p>
　　<p>&ensp;&ensp;据刘京介绍，过去的十六年中，《公益时报》秉着“旗帜鲜明地扬善”这一初心，以编制中国慈善榜的形式，完整记录下了十六年来我国大额捐赠的详尽数据和发展趋势，2845位慈善家和7223家慈善企业都曾榜上有名，收录大额捐赠总金额超过2000亿元。</p>
　　<p>&ensp;&ensp;第十七届（2020）中国慈善榜榜单显示，上榜慈善家共118位（对），合计捐赠54.5036亿元。平均捐赠额为4619万元，与前一年度相比，上涨近1200万元。</p>
　　<p>&ensp;&ensp;捐赠在1000万元以上的慈善家共63位（对），捐赠总额达到53.0270亿元，占到了总捐赠的97.29%。分析慈善家的资金来源可以看到，房地产行业依然是出产慈善家最多的领域。</p>
　　<p>&ensp;&ensp;上榜慈善企业共605家，合计捐赠124.0590亿元。其中投入教育领域的捐赠达到51.4419亿元，投入扶贫领域的捐赠则达到47.9936亿元。</p>
　　<p>&ensp;&ensp;上榜企业主要来自房地产、医药、金融、能源等领域，大额捐赠的主力仍是民营企业。365家内地民营企业共捐赠96.3312亿元，占捐赠总额的77.65%，平均每家企业捐赠2639万元。</p>
　　<p>&ensp;&ensp;除了对2019年的大额捐赠进行回顾，为了记录2020年中国人民同舟共济、守望相助、同心战疫的历程，活动还发布了抗击新冠肺炎疫情捐赠榜单。</p>
　　<p>&ensp;&ensp;2903家企业、机构与个人上榜，捐赠总额达到384.77亿元。其中捐赠额在1000万以上的捐赠者共409个，捐赠总额155.17亿元。</p>
　　<p>&ensp;&ensp;刘京表示，2019年是新中国成立70周年的年份，中国公益行业在这一年内见证了许多标志性的事件，它们与这场抗“疫”一道，展示了我国新时代公益的新特征：</p>
　　<p>&ensp;&ensp;第一，公益慈善在国家顶层设计中地位空前。第二，公益慈善管理框架趋于精细化。第三，公益慈善开始关注国家根基。第四，公众捐赠热情凝聚，大额捐赠趋于理性。</p>
