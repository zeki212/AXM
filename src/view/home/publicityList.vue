      <p>来自我国大西南地区“三区三州”产业扶贫的报告</p>
      <p>2020年7月10日</p>
      <p>&ensp;&ensp;“三区三州”是我国脱贫攻坚最难啃的“硬骨头”。如何实现脱贫目标？脱贫后如何可持续发展、稳步迈向小康之路？记者采访发现，在决战决胜脱贫攻坚的关键时点，我国大西南的“三区三州”地区从政府到企业、到群众，正因地制宜，积极探索产业扶贫、产业稳住脱贫成果、产业振兴乡村的路子，各类扶贫产业在“三区三州”落地生根。</p>
　　  <p>&ensp;&ensp;<b>产业支撑脱贫攻坚</b></p>
      <p>&ensp;&ensp;产业扶贫是撬动贫困地区发展、增加贫困农户收入的重要途径。近年来，在广大的“三区三州”地区，脱贫方式多种多样，而产业扶贫在深度贫困地区的脱贫中发挥了关键作用。</p>
　　  <p>&ensp;&ensp;在四川凉山彝族自治州，喜德县特合村第一书记刘骥告诉记者，通过整合产业发展资金和与公司合作，村里新建成了6个生猪代养场，每年可为每户贫困户分红1000元，在产业带动下，该村贫困户人均年收入已超万元，贫困发生率从29.6%降至2.19%。</p>
　　  <p>&ensp;&ensp;贫困人口从2013年的51.8万减少到2019年末的17.8万，贫困发生率在6年间降低到4%，产业对凉山脱贫的带动作用不可忽视。据统计，凉山全州种养大户已突破10万户，各类经营主体农户带动面达75%以上，50%以上的已脱贫人口中依靠发展农业产业实现脱贫。</p>
　　  <p>&ensp;&ensp;2016年以来，西藏累计投入362亿元实施扶贫产业项目2661个，带动了23.8万人脱贫。而在四川藏区，2019年甘孜藏族自治州投入财政扶贫专项资金132亿元，全面推进贫困村与非贫困村、贫困户与非贫困户统筹发展，实现7125户3.1万人脱贫，贫困发生率降至0.23%。</p>
　　<p>&ensp;&ensp;在云南迪庆藏族自治州香格里拉市，2015年以来，当地累计投入产业发展资金4.3亿元，中药材、特色畜禽、高原杂粮、蔬菜等优势产业不断发展壮大，综合贫困发生率降到了2019年底的0.38%……宜农则农、宜牧则牧、宜游则游，记者看到，产业扶贫作为脱贫攻坚的“第一工程”，筑牢了“三区三州”的脱贫根基。</p>
　　<p>&ensp;&ensp;<b>产业发展“准又稳”</b></p>
　　<p>&ensp;&ensp;在“三区三州”地区，复杂的地形地貌和气候条件在塑造了独特的自然景观的同时，也曾是制约当地产业发展的屏障。而如今，记者经常能听到“生态是最脆弱的资源、也是最宝贵的资源”的说法。依托生态本底，找准产业发展方向、把稳脱贫基础，农林产业、现代农业、扶贫车间、乡村旅游等不同产业发展，让困难群众从“看天吃饭”变为“靠产业吃饭”。</p>
　　<p>&ensp;&ensp;“工作的地方离家只有2公里，每月工资4000元，再也不用四处打工了。”今年45岁的索朗，在西藏日喀则市谢通门县达那答乡一家产业扶贫企业工作。他所在的企业主要从事食用菌生产加工，目前日产食用菌2吨至3吨，127名工人中68人是建档立卡贫困户。从田间地头到扶贫车间，扶贫产业链越来越完善，现代农业挑起了“三区三州”稳定脱贫的“金扁担”。</p>
　　<p>&ensp;&ensp;在四川凉山，绵阳市涪城区、佛山市禅城区对口帮扶工作领导小组与昭觉县共同组建的涪昭现代农业产业园区，已经建设了标准蔬菜、食用菌种植大棚500余亩，三年内将建成占地5000亩的现代农业园区。2019年，凉山东西部扶贫协作帮扶资金到位7.7亿元。</p>“打赢脱贫攻坚战，东西部产业合作是促进产业发展带动脱贫的重要举措。”从广东佛山市到凉山州昭觉县挂职县委副书记的徐航深有体会。
　　<p>&ensp;&ensp;而在三江并流的云南怒江傈僳族自治州，全州30个旅游扶贫示范村正加快建设，依托特色旅游资源培育750户旅游扶贫示范户。当地群众依靠旅游产业，实现了楼上居住楼下就业。</p>
　　<p>&ensp;&ensp;<b>为全面小康注入活力</b></p>
　　<p>&ensp;&ensp;产业扶贫，不仅是“三区三州”地区脱贫的“定心丸”，更是奔小康的“强心针”。</p>
　　<p>&ensp;&ensp;湾地沟村是四川甘孜州雅江县53个贫困村之一，全村有建档立卡贫困户16户52人。在有关方面的定点帮扶下，湾地沟村依托海拔相对较低、日照充足、林多地少、水质优良的自然条件，由帮扶单位、企业和村合作社共同出资360万元，在村里建设了大型养殖场，4年来共向村民分红80余万元，为贫困户增收脱贫起到了重要作用。帮扶干部张会东告诉记者，2019年，该村贫困户人均分红达到了1000元，非贫困户人均分红730元。</p>
　　<p>&ensp;&ensp;在海拔4250米的西藏那曲市色尼区罗玛镇普拉村，从草场深处搬迁到易地扶贫搬迁安置点的布次仁实现了养牦牛的梦想，成为牦牛牧场职工的一家三口人均年收入如今已经达到3万元。“我学到了养殖和牧场管理技术，儿子正在学习驾驶，希望成为牧场的驾驶员。”</p>
　　<p>&ensp;&ensp;手中有活干，奔小康更有信心。扶贫产业实现了深度贫困地区生态资源到产业价值的转换，如今，特色优势产业变成了“三区三州”地区的“金字招牌”，为脱贫奔小康注入了强大的竞争力。</p>
